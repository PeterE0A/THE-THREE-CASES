            public void HighFIVE()
            {

                int afleveringer = 10;

                if (afleveringer == 10)
                {
                    Console.Write("High Five – Jubel!!!");
                    Console.ReadLine();
                    Console.Clear();
                }
            }


        public void huh()
        {
            int afleveringer = 10;

            if (afleveringer <= 10)
            {
                Console.Write("Huh!");
                Console.ReadLine();
                Console.Clear();
            }
        }



        public void OLE()
            {
                int mål = 1;

                if (mål == 1)
                {
                    Console.Write("Olé olé olé");
                    Console.ReadLine();
                    Console.Clear();
                }


            }
        


       
            public void SHH()
            {
                int afleveringer = 0;

                if (afleveringer == 0)
                {
                    Console.Write("Shh");
                    Console.ReadLine();
                    Console.Clear();
                }


            }
       







    }

